# Вхідні дані: натуральне число 1<=n<=100
n = int(input())
for j in range (n+1): # Введемо для виведення даних у рядках "for" та "j"
    print(' '*(n-j) + '#'*j) # На кожному рядку поставимо спочатку пропуски а потім # в залежності від номеру рядка