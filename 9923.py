# На вхід програми подається натуральне число n та деякий текст
# Потрібно вивести цей текст у вигляді n рядків списку з маркером "-"
# Приклад   - Good
#           - Good
#           - Good...
# Вхідні дані: Натуральне число n в першому рядку і текст з латинських літер в другому.
n = int(input())
m = str(input())
for j in range (n): # Для запису в "рядках" використаємо "j" замість "i"
    print('-', m)