#Потрібно знайти і вивести кількість непарних цілих чисел, що лежать між числами a і b.
#Вхідні дані: Два цілих числа через проміжок
a,b = map(int,input().split())
c = abs((b-a))//2
if a%2!=0 and b%2!=0: #Якщо обидва крайніх числа - непарні,
    c-=1 # то між ними буде на 1 непарне число менше ніж половина від модуля різниці
    print(c)
else:
    print(c)