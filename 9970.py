# Очислити суму n перших чисел послідовності
# Вхідні дані: Натуральне число 1<=n<=100
n = int(input())
a = 0 # встановимо лічильник на 0
while n: # Додамо умову "While" для значень n > 0
    a+=n # Додамо до лічильника початкове значення n
    n-=1 # Зменшимо на одиницю число n і цикл повторимо
print (a)